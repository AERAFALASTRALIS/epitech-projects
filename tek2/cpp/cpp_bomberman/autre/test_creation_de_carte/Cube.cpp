void Cube::update(gdl::Clock const &clock, gdl::Input &input)
{
  if (input.getKey(SDLK_UP) || input.getKey(SDLK_z))
    translate(glm::vec3(0, 2, 0) * static_cast<float>(clock.getElapsed()) * _speed);
  if (input.getKey(SDLK_DOWN) || input.getKey(SDLK_x))
    translate(glm::vec3(0, -2, 0) * static_cast<float>(clock.getElapsed()) * _speed);
  if (input.getKey(SDLK_LEFT) || input.getKey(SDLK_q))
    translate(glm::vec3(2, 0, 0) * static_cast<float>(clock.getElapsed()) * _speed);
  if (input.getKey(SDLK_RIGHT) || input.getKey(SDLK_d))
    translate(glm::vec3(-2, 0, 0) * static_cast<float>(clock.getElapsed()) * _speed);
    if (input.getKey(SDLK_e))
      translate(glm::vec3(0, 0, 2) * static_cast<float>(clock.getElapsed()) * _speed);
  if (input.getKey(SDLK_c))
      translate(glm::vec3(0, 0, -2) * static_cast<float>(clock.getElapsed()) * _speed);

}

void Cube::draw(gdl::AShader &shader, gdl::Clock const &clock)
{
  (void)clock;
  _texture.bind();
  _geometry.draw(shader, getTransformation(), GL_QUADS);
}
