#include "Bomb.hh"

Bomb::Bomb(glm::vec3 const &position, glm::vec3 const &rotation, glm::vec3 const &scale, gdl::Texture const &texture, int hp) :
  AItem(position, rotation, scale, texture, hp)
{
    addEffect(AItem::Damage, AItem::OnSecond);
    addEffect(AItem::StartExplosionH, AItem::OnDeath);
    addEffect(AItem::StartExplosionV, AItem::OnDeath);
}

Bomb::Bomb(glm::vec3 const &position, glm::vec3 const &rotation, glm::vec3 const &scale, gdl::Texture const &texture, int hp, int id) :
  AItem(position, rotation, scale, texture, hp, id)
{
    addEffect(AItem::Damage, AItem::OnSecond);
    addEffect(AItem::StartExplosionH, AItem::OnDeath);
    addEffect(AItem::StartExplosionV, AItem::OnDeath);
}

Bomb::~Bomb() {}

