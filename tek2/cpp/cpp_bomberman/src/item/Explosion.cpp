#include "Explosion.hh"

Explosion::Explosion(glm::vec3 const &position, glm::vec3 const &rotation, glm::vec3 const &scale, gdl::Texture const &texture, int hp) :
  AItem(position, rotation, scale, texture, hp)
{
  addEffect(AItem::Damage, AItem::OnHalfSecond);
  addEffect(AItem::DestroyAll, AItem::OnPlay);
}

Explosion::~Explosion() {}

